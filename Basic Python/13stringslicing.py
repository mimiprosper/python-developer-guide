# Strings starts from 0,1....nth
# Or from the end -1,-2,-3.....-a
# Using the example index 0 & -8 gives the letter E
# Index 7 & -1 gives the same letter l

a = "Emmanuel"
print(a[-1])

# Prints index 0 to index 4
print(a[0:4])

# Prints index 1 to index 3
print(a[1:4])

# Prints index -8 to index -2
print(a[-8:-1])

# Prints index -5 to index -4
print(a[-5:-3])
